/************************************************************************
   normalize

   Input: msb_x --pointer to MSByte of x
         norm_a --pointer to byte length of x

   Output:
     pointer to first non-zero MSByte
     real byte length

*************************************************************************/
PUCHAR normalize(PUCHAR msb_x, unsigned int *norm_a)
{
 unsigned int sz_a;
 PUCHAR  msb_a;
 sz_a = *norm_a;
 msb_a = msb_x;
 //
 //Is the byte length non-zero and MSByte non-zero then done,
 //else search the length for the first non-zero byte
 //
 if((sz_a) && (!(*msb_a)))
 {
  while(sz_a)
  {
   if(*msb_x)
    break;
   else
   {
    msb_x++;
    sz_a--;
   }
  }
 }
 *norm_a = sz_a;
 return(msb_x);
}


/************************************************************************
   compareFast
   Input: msb_x --pointer to MSByte of x
          msb_y --pointer to MSByte of y
           sz_x --byte count of x (may include leading zeros)
           sz_y --byte count of y (may include leading zeros)
   Output:
     0  -> x==y
    -1  -> x<y
     1  -> x>y

*************************************************************************/
INT compareFast(PUCHAR msb_x, unsigned int sz_x,
                PUCHAR msb_y, unsigned int sz_y)
{

 
  unsigned int sz_t,sz_u;
  PUCHAR msb_t,msb_u;

  sz_t = sz_x;
  sz_u = sz_y;

  msb_t = normalize(msb_x,&sz_t);
  msb_u = normalize(msb_y,&sz_u);
  if (sz_t > sz_u)
   return(1);
  if (sz_u > sz_t)
   return(-1);
  //
  //Same size, if the sizes are zero then done,
  //else keep comparing from MSByte to LSByte
  //
  if(sz_t && sz_u)
  {
   do{
      if((*msb_t) != (*msb_u))
       break;
	  msb_t++;
	  msb_u++;
   }while(sz_t--);
   if(sz_t)
    if((*msb_t) > (*msb_u))
     return(1);
    else
     return(-1);
   else
    return(0);
  }
  return(0);
}

/************************************************************************
   verifyRSAModExpo

        Check the input parameters,key and user data consistency, these
        are hardware requirements that will cause hardware errors when
        parameters are out of bounds.


                 - Verify the supplied parameters
                 - Verify exponent and modulus are odd
                 - Verify input is less then modulus


*************************************************************************/
icaStatus_t verifyRSAModExpo(unsigned int          inputDataLength,
                             unsigned int          outputDataLength,
                             unsigned char         *pInputData,
                             ICA_KEY_RSA_MODEXPO   *pKeyModExpo)
{

  PUCHAR                  pUserData;
  PUCHAR                  pModulus;
  unsigned int            modulusByteLength;
  unsigned int            modulusHwByteLength;
  ICA_KEY_RSA_MODEXPO     keyME;              // need this to convert Big->little endian

 /***********************************************************
 ****BigEndian to Little Endian Conversion               ****
 **** Bit length to Byte length (round up)               ****
 **** OutputDataLength must be sufficient to hold result ****
 ************************************************************/
 REVERSE_UINT(&(keyME.modulusBitLength), &(pKeyModExpo->modulusBitLength));
 modulusByteLength = (keyME.modulusBitLength+7)/8;  // determine Modulus number of bytes

 if (modulusByteLength > outputDataLength)
 {
   printf("\n modulusByteLength(0x%08lx) outputDataLength(0x%08lx)", modulusByteLength,outputDataLength);
   return(HDDInvalidLength);
 }

 /***********************************************
 ****BigEndian to Little Endian Conversion *****
 **** Get the keyType                      *****
 ***********************************************/
 REVERSE_UINT(&(keyME.keyType), &(pKeyModExpo->keyType));

 if ((keyME.keyType != RSA_PUBLIC_MODULUS_EXPONENT) &&
     (keyME.keyType != RSA_PRIVATE_MODULUS_EXPONENT))
 {
    return(PKABadParm);
 }

 //
 // Is the supplied Token length less then the calculated length
 // rsaRef[index].keylength = nLength + expLength (supplied in token)
 // then add the non-key structure references(header+lengths+offsets)
 //
 REVERSE_UINT(&(keyME.keyLength), &(pKeyModExpo->keyLength));
 REVERSE_UINT(&(keyME.expLength), &(pKeyModExpo->expLength));
 REVERSE_UINT(&(keyME.nLength), &(pKeyModExpo->nLength));

 if(keyME.keyLength < (keyME.expLength + keyME.nLength +
                       SZ_HEADER_MODEXPO))
 {
   return(HDDInvalidLength);
 }
 //
 // Does the expLength & nLength meet the hardware requirements
 // all must be multiple of 32 bytes(256 bits)
 //
 modulusHwByteLength = ((modulusByteLength+31)/32)*32;
 if((keyME.expLength != modulusHwByteLength) ||
    (keyME.nLength != modulusHwByteLength))
 {
   return(HDDInvalidLength);
 }

 //
 //RSA Modular Exponentiation rules & hardware limits
 // x   exponent must be odd
 // x   modulus must be odd
 // x   0 <= input Message < Modulus
 //     0 <= exponent < 2**bit length---Not Implemented??
 //     0 <= modulus  < 2**bit length---Not Implemented??

 //
 // exponent odd?
 //
 REVERSE_UINT(&(keyME.expOffset), &(pKeyModExpo->expOffset));
 if (!(*((PUCHAR)pKeyModExpo + keyME.expOffset + (keyME.expLength-1)) & 0x01))
 {
    return(PKABadParm);
 }
 //
 // modulus odd?
 //
 REVERSE_UINT(&(keyME.nOffset), &(pKeyModExpo->nOffset));
 if (!(*((PUCHAR)pKeyModExpo + keyME.nOffset + (keyME.nLength-1)) & 0x01))
 {
    return(PKABadParm);
 }
 //
 // 0 <= input Message < Modulus?
 // Message & Modulus are BigEndian
 //
 //
 // find MSbyte of Modulus(note: may have leading zeros)
 // compare the value of Modulus to value of input data
 //
 pModulus = (PUCHAR)pKeyModExpo + keyME.nOffset;
 pUserData = pInputData;
 if((compareFast(pModulus,keyME.nLength,pUserData,inputDataLength)) <= 0)
 {
    return(PKABadParm);
 }

 return(HDDGood);
} // verifyRSAModExp

/************************************************************************
   verifyRSACrt
                 - Verify the RSA key & Data


*************************************************************************/
icaStatus_t verifyRSACrt(unsigned int          inputDataLength,
                         unsigned int          outputDataLength,
                         unsigned char         *pInputData,
                         ICA_KEY_RSA_CRT       *pKeyCrt)
{

  PUCHAR                  pPrimeP;
  PUCHAR                  pPrimeQ;
  PUCHAR                  qInverse;
  unsigned int            modulusByteLength;
  ICA_KEY_RSA_CRT         keyCRT;        // need this to convert Big->little endian

 /***********************************************************
 ****BigEndian to Little Endian Conversion               ****
 **** Bit length to Byte length (round up)               ****
 **** OutputDataLength must be sufficient to hold result ****
 ************************************************************/
 REVERSE_UINT(&(keyCRT.modulusBitLength), &(pKeyCrt->modulusBitLength));
 modulusByteLength = (keyCRT.modulusBitLength+7)/8;  // determine Modulus number of bytes

 if (modulusByteLength > outputDataLength)
 {
   return(HDDInvalidLength);
 }

 /***********************************************
 ****BigEndian to Little Endian Conversion *****
 **** Get the keyType                      *****
 ***********************************************/
 REVERSE_UINT(&(keyCRT.keyType), &(pKeyCrt->keyType));

 if (keyCRT.keyType != RSA_PKCS_PRIVATE_CHINESE_REMAINDER)
 {
    return(PKABadParm);
 }

 //
 // Is the supplied Token length less then the calculated length?
 //
 REVERSE_UINT(&(keyCRT.keyLength), &(pKeyCrt->keyLength));
 REVERSE_UINT(&(keyCRT.pLength), &(pKeyCrt->pLength));
 REVERSE_UINT(&(keyCRT.qLength), &(pKeyCrt->qLength));
 REVERSE_UINT(&(keyCRT.dpLength), &(pKeyCrt->dpLength));
 REVERSE_UINT(&(keyCRT.dqLength), &(pKeyCrt->dqLength));
 REVERSE_UINT(&(keyCRT.qInvLength), &(pKeyCrt->qInvLength));

 if(keyCRT.keyLength < (keyCRT.pLength + keyCRT.qLength +
                        keyCRT.dpLength + keyCRT.dqLength +
                        keyCRT.qInvLength +
                        SZ_HEADER_CRT))
 {
   return(HDDInvalidLength);
 }
 //
 //RSA CRT rules & hardware limits
 //
 //  x  p must be odd
 //  x  q must be odd
 //  x  dp must be odd
 //  x  dq must be odd
 //  x  p>q
 //  x  sizeof(p)+sizeof(q) <= modulus bit length
 //  x  3<=p<2**1087
 //  x  3<=q<2**1084
 //  x  1<=qInv<p
 //     1<=dp<(p-1)
 //     1<=dq,(q-1)
 //     0<=m<(p*q)
 //
 // prime p odd?
 //
 REVERSE_UINT(&(keyCRT.pOffset), &(pKeyCrt->pOffset));
 if (!(*((PUCHAR)pKeyCrt + keyCRT.pOffset + (keyCRT.pLength-1)) & 0x01))
 {
    return(PKABadParm);
 }
 //
 // prime q odd?
 //
 REVERSE_UINT(&(keyCRT.qOffset), &(pKeyCrt->qOffset));
 if (!(*((PUCHAR)pKeyCrt + keyCRT.qOffset + (keyCRT.qLength-1)) & 0x01))
 {
    return(PKABadParm);
 }
 //
 // subprime dp odd?
 //
 REVERSE_UINT(&(keyCRT.dpOffset), &(pKeyCrt->dpOffset));
 if (!(*((PUCHAR)pKeyCrt + keyCRT.dpOffset + (keyCRT.dpLength-1)) & 0x01))
 {
    return(PKABadParm);
 }
 //
 // subprime dq odd?
 //
 REVERSE_UINT(&(keyCRT.dqOffset), &(pKeyCrt->dqOffset));
 if (!(*((PUCHAR)pKeyCrt + keyCRT.dqOffset + (keyCRT.dqLength-1)) & 0x01))
 {
    return(PKABadParm);
 }
 //
 // p > q ?
 //
 // find MSbyte of p(note: may have leading zeros)
 //
 pPrimeP = (PUCHAR)pKeyCrt + keyCRT.pOffset;
 pPrimeQ = (PUCHAR)pKeyCrt + keyCRT.qOffset;
 if((compareFast(pPrimeP,keyCRT.pLength,pPrimeQ,keyCRT.qLength)) <= 0)
 {
    printf("\n FAIL p > q test");
    return(PKABadParm);
 }
 //
 //  sizeof(p)+sizeof(q) <= modulus bit length?
 //
 if((keyCRT.pLength + keyCRT.qLength) >  keyCRT.modulusBitLength)
 {
    printf("\n FAIL sizeof(p)+sizeof(q) <= modulus bit length?");
    return(PKABadParm);
 }
 //
 //    3<=p<2**1087?
 //
   if((compareFast(pPrimeP,keyCRT.pLength,exp1087,SZ_EXP1087)) >= 0)
   {
      printf("\n FAIL p > 2**1087");
      return(PKABadParm);
   }
   if((compareFast(pPrimeP,keyCRT.pLength,three,SZ_THREE)) < 0)
   {
      printf("\n FAIL p < 3");
      return(PKABadParm);
   }
 //
 //    3<=q<2**1084?
 //
   if((compareFast(pPrimeQ,keyCRT.qLength,exp1084,SZ_EXP1084)) >= 0)
   {
      printf("\n FAIL q > 2**1084 test");
      return(PKABadParm);
   }
   if((compareFast(pPrimeQ,keyCRT.qLength,three,SZ_THREE)) < 0)
   {
      printf("\n FAIL q < 3 test");
      return(PKABadParm);
   }
 //
 //    1<=qInv<p
 //
   REVERSE_UINT(&(keyCRT.qInvOffset), &(pKeyCrt->qInvOffset));
   qInverse = (PUCHAR)pKeyCrt + keyCRT.qInvOffset;
   if((compareFast(qInverse,keyCRT.qInvLength,pPrimeP,keyCRT.pLength)) >= 0)
   {
      printf("\n FAIL p > qInv test");
      return(PKABadParm);
   }
   if((compareFast(qInverse,keyCRT.qInvLength,one,SZ_ONE)) < 0)
   {
      printf("\n FAIL qInv > 1 test");
      return(PKABadParm);
   }

 return(HDDGood);
} // verifyRSAModCrt

