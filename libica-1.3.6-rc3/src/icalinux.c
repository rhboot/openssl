/* AES                                                               */
/*-------------------------------------------------------------------*/
int zAes(ica_aes_t *pAes, unsigned int keysLen)
{
	struct sigaction new, old;
	sigset_t newset, oldset;
	int rv = 0;
	int local_rc;
	unsigned long function_code;
        unsigned char keybuff[sizeof(ICA_AES_VECTOR)
			      + sizeof(ICA_KEY_AES_LEN256)];
	unsigned int output_data_len;
	unsigned int aes_key_len;

	// Check input data length
	if (pAes->inputdatalength & 0x07) {
		return HDDInvalidParm;
	}

	// Verify that we have the key length requested.
	switch (keysLen) {
		case AES_KEY_LEN128:
			if (!aes128_switch) {
				local_rc = icaAesSW(pAes->mode,
						    pAes->inputdatalength,
						    (pAes->direction
						     == DEVICA_DIR_DECRYPT
						     ? 0 : 1), pAes->inputdata,
						    (unsigned char *)pAes->iv,
						    128,
						    (unsigned char *)pAes->keys,
						    &output_data_len,
						    pAes->outputdata);
				if (local_rc)
					return ENODEV;
				else
					return 0;
			}
			break;
		case AES_KEY_LEN192:
			if (!aes192_switch) {
				local_rc = icaAesSW(pAes->mode,
						    pAes->inputdatalength,
						    (pAes->direction
						     == DEVICA_DIR_DECRYPT
						     ? 0 : 1), pAes->inputdata,
						    (unsigned char *)pAes->iv,
						    192,
						    (unsigned char *)pAes->keys,
						    &output_data_len,
						    pAes->outputdata);
				if (local_rc)
					return ENODEV;
				else
					return 0;
			}
			break;
		case AES_KEY_LEN256:
			if (!aes256_switch) {
				local_rc = icaAesSW(pAes->mode,
						    pAes->inputdatalength,
						    (pAes->direction
						     == DEVICA_DIR_DECRYPT
						     ? 0 : 1), pAes->inputdata,
						    (unsigned char *)pAes->iv,
						    256,
						    (unsigned char *)pAes->keys,
						    &output_data_len,
						    pAes->outputdata);
				if (local_rc)
					return ENODEV;
				else
					return 0;
			}
			break;
		default:
			return HDDInvalidParm;
	}

	switch(pAes->mode){
		case DEVICA_MODE_CBC:
			// Copy caller's chaining vector and keys to local stge
			memcpy(keybuff, (void *)pAes->iv,
			       sizeof(ICA_AES_VECTOR));
			memcpy(keybuff + sizeof(ICA_AES_VECTOR), pAes->keys,
			       keysLen);
			break;

		case DEVICA_MODE_ECB:
			// Copy the caller's keys to local storage
			memcpy(keybuff, pAes->keys, keysLen);
			break;

		default:
			return HDDInvalidParm;
	} // end switch mode

	// Compute the function code
	// AES uses 18-20 decimal, corresponding to lengths 16, 24, 32.
	function_code = 0x10 + (keysLen/8);
	switch (pAes->direction) {
		case DEVICA_DIR_DECRYPT:
			function_code += 0x80;
			break;
		case DEVICA_DIR_ENCRYPT:
			break;
		default:
			return HDDInvalidParm;
	}

	if (extra_sigill_checks) {
		sigemptyset(&newset);
		sigaddset(&newset, SIGILL);
		sigprocmask(SIG_UNBLOCK, &newset, &oldset);
		new.sa_handler = (void *) sigill_handler;
		new.sa_flags = 0;
		sigaction(SIGILL, &new, &old);
		if (setjmp(envq) != 0) {
			des_switch = 0;
			tdes_switch = 0;
			aes128_switch = 0;
			aes192_switch = 0;
			aes256_switch = 0;
			prng_switch = 0;
			sigaction(SIGILL, &old, NULL);
			sigprocmask(SIG_SETMASK, &oldset, NULL);
			return EXCEPTION_RV;
		}
	}

	if (pAes->mode == DEVICA_MODE_CBC) {
		rv = KMC(function_code,
			 keybuff,
			 pAes->outputdata,
			 pAes->inputdata,
			 pAes->inputdatalength);
	} else {
		rv = KM(function_code,
			keybuff,
			pAes->outputdata,
			pAes->inputdata,
			pAes->inputdatalength);
	}

	if (extra_sigill_checks) {
		sigaction(SIGILL, &old, NULL);
		sigprocmask(SIG_SETMASK, &oldset, NULL);
	}

	return rv;
} /* end zAes */

