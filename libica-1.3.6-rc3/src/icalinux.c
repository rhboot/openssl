int queryEnvExtraSigillChecks(void)
{
	// If there is an environment variable, add extra checks.
	if (getenv("LIBICA_SIGILL_CHECKS") != NULL)
		return 1;

	// Otherwise, no extra checks.
	return 0;
}

int extra_sigill_checks = 0;

void queryCryptoAssist(void)
{
	struct sigaction new, old;
	sigset_t newset, oldset;

	if (cpacf_checked)
		return;

	extra_sigill_checks = queryEnvExtraSigillChecks();

	cpacf_checked = 1;
	sha1_switch = 0;
	sha256_switch = 0;
	des_switch = 0;
	tdes_switch = 0;
	aes128_switch = 0;
	aes192_switch = 0;
	aes256_switch = 0;
	prng_switch = 0;
	sigemptyset(&newset);
	sigaddset(&newset, SIGILL);
	sigprocmask(SIG_UNBLOCK, &newset, &oldset);
	new.sa_handler = (void *) sigill_handler;
	new.sa_flags = 0;
	sigaction(SIGILL, &new, &old);

	if (setjmp(envq) == 0) {
		queryHash();
		queryCipher();
	}
	sigaction(SIGILL, &old, NULL);
	sigprocmask(SIG_SETMASK, &oldset, NULL);

	if (prng_switch) {
		int rc, handle = -1;
		do {
			unsigned char seed[16];
			rc = open("/dev/urandom", O_RDONLY);
			if (rc == -1)
				break;
			handle = rc;
			rc = read(handle, seed, sizeof(seed));
			if (rc == -1)
				break;
			rc = zPRNG_Seed_Random(seed, sizeof(seed)/DWORD);
			if (rc) {
				printf("zPRNG_Seed_Random(%d) FAILED with "
				       "rc = %02X!\n", sizeof(seed), rc);
				break;
			}
		} while (0);
		if (handle != -1)
			close(handle);
		// If the original seeding failed, we should try to stir in some
		// entropy anyway (since we already put out a message).
		if (rc) {
			if ((rc = zPRNG_Seed_Random(NULL, 0)) != 0)
				printf("zPRNG_Seed_Random(0) FAILED with "
				       "rc = %02X!\n", rc);
		}
	}
}
